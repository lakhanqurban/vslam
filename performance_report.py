import json
from pathlib import Path
from datetime import datetime
def generate_comprehensive_report(metrics: dict) -> str:
    """
    Generate a comprehensive text-based performance report.
    
    Args:
        metrics: Dictionary containing performance metrics
        
    Returns:
        Formatted text report
    """
    report = "=" * 80 + "\n"
    report += "VISUAL SLAM DEMO - COMPREHENSIVE PERFORMANCE REPORT\n"
    report += "=" * 80 + "\n\n"
    
    report += f"📊 EXECUTIVE SUMMARY\n"
    report += f"────────────────────\n"
    report += f"• Total Frames Processed: {metrics['frames_processed']:,}\n"
    report += f"• Keyframes Extracted: {metrics['keyframes_stored']:,}\n"
    report += f"• Loop Closures Detected: {metrics['loop_closures_detected']:,}\n"
    report += f"• Final Trajectory Accuracy: {metrics['refined_drift_m']:.3f} m drift\n"
    report += f"• Performance Grade: {'A' if metrics['refined_drift_m'] < 0.5 else 'B' if metrics['refined_drift_m'] < 1.5 else 'C'}\n\n"
    
    report += f"🧪 DETAILED PERFORMANCE METRICS\n"
    report += f"────────────────────\n"
    report += f"🖥 Raw Performance:\n"
    report += f"   • Initial Drift: {metrics['initial_drift_m']:.3f} m\n"
    report += f"   • Refined Drift: {metrics['refined_drift_m']:.3f} m\n"
    report += f"   • Total Reduction: {metrics['drift_reduction_percentage']:.1f}%\n"
    report += f"   • Loop Closure Interval: {metrics['average_loop_closure_interval']:.1f} frames\n\n"
    
    report += f"📊 Perception Quality:\n"
    report += f"   • Average Inlier Ratio: {metrics['average_inlier_ratio']:.3f}\n"
    report += f"   • Range: [{metrics['min_inlier_ratio']:.3f}, {metrics['max_inlier_ratio']:.3f}]\n"
    report += f"   • Average Feature Count: {metrics['average_feature_count']:.1f}\n"
    report += f"   • Range: [{metrics['min_feature_count']}, {metrics['max_feature_count']}]\n\n"
    
    report += f"🏆 Comparative Analysis (Synthetic vs Real Data)\n"
    report += f"────────────────────\n"
    synth = metrics["benchmark_summary"]["synthetic_performance"]
    real = metrics["benchmark_summary"]["real_data_performance"]
    gaps = metrics["benchmark_summary"]["performance_gaps"]
    
    report += f"SYNTHETIC MODE:\n"
    report += f"   • Final Drift: {synth['final_drift_m']} m\n"
    report += f"   • Drift Reduction: {synth['drift_reduction_pct']}%\n"
    report += f"   • Loop Closure Rate: {synth['loop_closure_rate']}\n"
    report += f"   • Feature Precision: {synth['feature_precision']}\n\n"
    
    report += f"REAL DATA MODE:\n"
    report += f"   • Final Drift: {real['final_drift_m']} m\n"
    report += f"   • Drift Reduction: {real['drift_reduction_pct']}%\n"
    report += f"   • Loop Closure Rate: {real['loop_closure_rate']}\n"
    report += f"   • Feature Precision: {real['feature_precision']}\n\n"
    
    report += f"PERFORMANCE GAPS:\n"
    report += f"   • Drift Improvement Difference: {gaps['drift_improvement_difference']:.1f}%\n"
    report += f"   • Reliability Equivalence: {'✅ Yes' if gaps['reliability_equivalence'] else '❌ No'}\n"
    report += f"   • Precision Equivalence: {'✅ Yes' if gaps['precision_equivalence'] else '❌ No'}\n\n"
    
    report += f"🔑 KEY RESEARCH INSIGHTS\n"
    report += f"────────────────────\n"
    for i, insight in enumerate(metrics['benchmark_summary']['key_insights'], 1):
        report += f"{i}. {insight}\n\n"
    
    report += f"🏆 RESEARCH APPLICATION SCORES\n"
    report += f"────────────────────\n"
    for category, score in metrics['research_application_score'].items():
        category_name = category.replace('_', ' ').title()
        report += f"• {category_name}: {score*100:.0f}%\n"
    
    report += f"\n✅ QUALITY GATE STATUS\n"
    report += f"────────────────────\n"
    for gate, status in metrics['quality_gate_status'].items():
        symbol = "✅" if status else "❌"
        gate_name = gate.replace('_', ' ').title()
        report += f"{symbol} {gate_name}: {'Pass' if status else 'Fail'}\n"
    
    report += f"\n"
    report += f"=" * 80 + "\n"
    report += f"END OF PERFORMANCE REPORT - Generated by Enhanced Metrics System\n"
    report += f"=" * 80
    return report


