def create_metrics_csv(metrics: dict, output_path: str) -> None:
    """
    Create CSV format for easy analysis.
    
    Args:
        metrics: Dictionary containing performance metrics
        output_path: Path to save the CSV file
    """
    import pandas as pd
    
    # Key performance indicators as rows
    rows = []
    
    # Basic metrics
    rows.append({
        'Category': 'Basic Performance',
        'Metric': 'Frames Processed',
        'Value': metrics['frames_processed']
    })
    rows.append({
        'Category': 'Basic Performance',
        'Metric': 'Keyframes Stored',
        'Value': metrics['keyframes_stored']
    })
    rows.append({
        'Category': 'Basic Performance',
        'Metric': 'Loop Closures Detected',
        'Value': metrics['loop_closures_detected']
    })
    
    # Trajectory accuracy
    rows.append({
        'Category': 'Trajectory Accuracy',
        'Metric': 'Initial Drift (m)',
        'Value': metrics['initial_drift_m']
    })
    rows.append({
        'Category': 'Trajectory Accuracy',
        'Metric': 'Final Drift (m)',
        'Value': metrics['refined_drift_m']
    })
    rows.append({
        'Category': 'Trajectory Accuracy',
        'Metric': 'Drift Reduction (%)',
        'Value': metrics['drift_reduction_percentage']
    })
    
    # Perception metrics
    rows.append({
        'Category': 'Perception Quality',
        'Metric': 'Average Inlier Ratio',
        'Value': metrics['average_inlier_ratio']
    })
    rows.append({
        'Category': 'Perception Quality',
        'Metric': 'Average Feature Count',
        'Value': metrics['average_feature_count']
    })
    
    # Benchmark comparison
    synth = metrics['benchmark_summary']['synthetic_performance']
    real = metrics['benchmark_summary']['real_data_performance']
    gaps = metrics['benchmark_summary']['performance_gaps']
    
    rows.append({
        'Category': 'Benchmark Comparison',
        'Metric': 'Synthetic Final Drift (m)',
        'Value': synth['final_drift_m']
    })
    rows.append({
        'Category': 'Benchmark Comparison',
        'Metric': 'Real Data Final Drift (m)',
        'Value': real['final_drift_m']
    })
    rows.append({
        'Category': 'Benchmark Comparison',
        'Metric': 'Drift Improvement Difference',
        'Value': gaps['drift_improvement_difference']
    })
    
    # Research scores
    for category, score in metrics['research_application_score'].items():
        category_name = category.replace('_', ' ').title()
        rows.append({
            'Category': 'Research Application',
            'Metric': f'{category_name}',
            'Value': f'{score*100:.0f}%'
        })
    
    # Create DataFrame and save
    df = pd.DataFrame(rows)
    df.to_csv(output_path, index=False)


